from abc import ABC, abstractmethod
from typing import Optional, List
import doctest


class ТранспортноеСредство(ABC):
    """
    Абстрактный класс транспортного средства.

    >>> class Автомобиль(ТранспортноеСредство):
    ...     def запустить_двигатель(self): ...
    ...     def остановить_двигатель(self): ...
    ...     def заправить(self, литры): ...
    >>> авто = Автомобиль("Toyota", 2022, 150)
    >>> авто.марка
    'Toyota'
    """

    def init(self, марка: str, год_выпуска: int, мощность: int):
        if not марка:
            raise ValueError("Марка не может быть пустой")
        if год_выпуска < 1900 or год_выпуска > 2025:
            raise ValueError("Некорректный год выпуска")
        if мощность <= 0:
            raise ValueError("Мощность должна быть положительной")

        self.марка = марка
        self.год_выпуска = год_выпуска
        self.мощность = мощность
        self.скорость = 0

    @abstractmethod
    def запустить_двигатель(self) -> str:
        """Запустить двигатель."""
        ...

    @abstractmethod
    def остановить_двигатель(self) -> bool:
        """Остановить двигатель."""
        ...

    @abstractmethod
    def заправить(self, литры: float) -> float:
        """Заправить топливо."""
        if литры <= 0:
            raise ValueError("Количество топлива должно быть положительным")
        ...


class КомпьютернаяИгра(ABC):
    """
    Абстрактный класс компьютерной игры.

    >>> class Шутер(КомпьютернаяИгра):
    ...     def начать_уровень(self, номер): ...
    ...     def сохранить_прогресс(self): ...
    ...     def завершить_игру(self): ...
    >>> игра = Шутер("Half-Life", "Valve", 1998)
    >>> игра.название
    'Half-Life'
    """

    def init(self, название: str, разработчик: str, год_релиза: int):
        if not название:
            raise ValueError("Название не может быть пустым")
        if not разработчик:
            raise ValueError("Разработчик не может быть пустым")
        if год_релиза < 1970 or год_релиза > 2025:
            raise ValueError("Некорректный год релиза")

        self.название = название
        self.разработчик = разработчик
        self.год_релиза = год_релиза
        self.текущий_уровень = 1
        self.завершена = False

    @abstractmethod
    def начать_уровень(self, номер: int) -> None:
        """Начать уровень."""
        if номер < 1:
            raise ValueError("Номер уровня должен быть положительным")
        ...

    @abstractmethod
    def сохранить_прогресс(self) -> dict:
        """Сохранить прогресс игры."""
        ...

    @abstractmethod
    def завершить_игру(self) -> str:
        """Завершить игру."""
        ...


class МузыкальныйАльбом(ABC):
    """
    Абстрактный класс музыкального альбома.

    >>> class РокАльбом(МузыкальныйАльбом):
    ...     def воспроизвести_трек(self, номер): ...
    ...     def оценить_альбом(self, оценка): ...
    ...     def добавить_в_плейлист(self): ...
    >>> альбом = РокАльбом("Abbey Road", "The Beatles", 1969, 17)
    >>> альбом.название
    'Abbey Road'
    """

    def init(self, название: str, исполнитель: str, год: int, треков: int):
        if not название:
            raise ValueError("Название не может быть пустым")
        if not исполнитель:
            raise ValueError("Исполнитель не может быть пустым")
        if год < 1900 or год > 2025:
            raise ValueError("Некорректный год")
        if треков <= 0:
            raise ValueError("Количество треков должно быть положительным")

        self.название = название
        self.исполнитель = исполнитель
        self.год = год
        self.треков = треков
        self.рейтинг = 0

    @abstractmethod
    def воспроизвести_трек(self, номер: int) -> str:
        """Воспроизвести трек."""
        if номер < 1 or номер > self.треков:
            raise ValueError("Некорректный номер трека")
        ...

    @abstractmethod
    def оценить_альбом(self, оценка: int) -> float:
        """Оценить альбом."""
        if оценка < 1 or оценка > 10:
            raise ValueError("Оценка должна быть от 1 до 10")
        ...

    @abstractmethod
    def добавить_в_плейлист(self, название_плейлиста: str) -> bool:
        """Добавить альбом в плейлист."""
        if not название_плейлиста:
            raise ValueError("Название плейлиста не может быть пустым")
        ...


